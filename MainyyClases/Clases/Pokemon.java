
package Clases;

public class Pokemon {
	
	private String nombre;
	private String habitad;
	
	public Pokemon() {
		
	}
	
	public Pokemon(String nombre, String habitad) {
		super();
		this.nombre = nombre;
		this.habitad = habitad;
	}

	public String getNombre() {
		return nombre;
	}

	public String getHabitad() {
		return habitad;
	}
	
	
	
	

}
